    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed


        String nome = Nome.getText();
        Adm = D_admissao.getText();
        String VHT = VHtrabalhada.getText();
        String HT = Htrabalhada.getText();
        if (nome.isBlank()) {
            JOptionPane.showMessageDialog(null, "Por favor, informe seu nome");
            label.setForeground(Color.red);
            Nome.setForeground(Color.red);
            Nome.requestFocus();
            
        } else if (Adm.isBlank() || D_admissao.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Por favor, informe sua data de admissao");
            jLabel4.setForeground(Color.red);
            D_admissao.setForeground(Color.red);
            D_admissao.requestFocus();
        } else if (VHT.isBlank()) {
            JOptionPane.showMessageDialog(null, "Por favor, informe o valor da hora trabalhada");
            jLabel5.setForeground(Color.red);
            VHtrabalhada.setForeground(Color.red);
            VHtrabalhada.requestFocus();
        } else if (HT.isBlank()) {
            JOptionPane.showMessageDialog(null, "Por favor, informe quantas horas trabalhou");
            jLabel6.setForeground(Color.red);
            Htrabalhada.setForeground(Color.red);
            Htrabalhada.requestFocus();
        }   else{
        try {    
                form.setLenient(false);
                Date Admissao = form.parse(Adm);
                f.setDataAdmissao(Admissao);
                f.setNome(nome);
                f.setValor_horas(Float.parseFloat(VHT));
                f.setHoras_trabalhadas(Float.parseFloat(HT));
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Data Inválida!");
                jLabel4.setForeground(Color.red);
                D_admissao.setForeground(Color.red);
                D_admissao.requestFocus();
            }
            int tempoEmpresa = f.calcularTempoempresa();
            float salario = f.calcularSalario();
            
            JOptionPane.showMessageDialog(null,"Nome: " + f.getNome()
                    + "\ndata de admissão: " + f.getDataAdmissao()
                    + "\nvalor da hora trabalhada: " + f.getValor_horas()
                    + "\nhoras trabalhadas: " + f.getHoras_trabalhadas()
                    + "\nTempo na empresa: " + tempoEmpresa
                    + "\nSalario: " + salario);

            if (c.Salvar(f)) {
                JOptionPane.showMessageDialog(null, "cadastro realizado");
                Nome.setText(" ");
                D_admissao.setText(" ");
                VHtrabalhada.setText(" ");
                Htrabalhada.setText(" ");
                Nome.requestFocus(); 
            } else {
                JOptionPane.showMessageDialog(null, "erro");
            }
        
        }

    }//GEN-LAST:event_jButton4ActionPerformed

